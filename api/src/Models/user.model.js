import mongoose from "mongoose";

const useSchema = new mongoose.Schema({
    username: {
        type: String,
        require: true,
        trim: true,
    },
    email: {
        type: String,
        require: true,
        unique: true,
        trim: true
    },
    password: {
        type: String,
        require: true
    },
    profile: {
        type: String,
        require: true
    }

}, {
    timestamps: true
})

export default mongoose.model("User", useSchema)